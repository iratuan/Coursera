package org.coursera.ita.java.entities;

public class Achievement {
	private String name;
	
	
	public Achievement() {
	
	}
	
	public Achievement(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
